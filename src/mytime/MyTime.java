package mytime;

public class MyTime {
     private int hours;
     private int min;

     // GETTERS --------------
     public int getHours() {
         return hours;
     }

     public int getMin() {
         return min;
     }

     // SETTERS --------------
     public void setHours(int hours) {
         this.hours = hours;
     }

     public void setMin(int min) {
         this.min = min;
     }



}
